        <h2>Des services</h2>
        <h3>Accès internet</h3>
        <p>Les ligues disposent d'un accès Internet mutualisé que la M2L loue à un prestataire extérieur</p>
        <h3>Accès wifi</h3>
        <p>Dans tous les espaces, un réseau Wifi "visiteurs" devra être disponible, avec une clé WPA renouvelée régulièrement et communiquée aux ligues. Ce réseau ne permettra que l'accès à Internet.</p>
        <h3>Téléphonie</h3>
        <p>Dans les bâtiments, les salles et bureaux sont équipés de prises de téléphone analogiques. La M2L y fournit les combinés téléphoniques.</p>
        <h3>Affranchissement</h3>
        <p>Une machine à affranchir permet un affranchissement rapide et en nombre. Cette prestation est facturée aux ligues au coût de l'affranchissement</p>
        <h3>Impressions</h3>
        <p>Les ligues disposent de la possibilité d'imprimer sur des ressources d'impression numériques connectées situées dans le local reprographie du rez-de-chaussée.</p>
        <h3>Serveur FTP documentaire et sauvegarde</h3>
        <p>La M2L met à disposition des ligues un serveur FTP documentaire intranet par étage regroupant des textes légaux, des modèles de dossiers, de statuts, des programmes de formation (...) compilés par le CROSL. Ce serveur devra également faire office de serveur de sauvegardes.</p>
        <h3>Gestion de la FAQ</h3>
        <p>La M2L met à disposition des ligues via l’intranet un site web personnalisé présentant leur activité. Tout internaute devra pouvoir se connecter à l’un des sites afin de poser des questions et consulter la FAQ . Pour cela il devra s’inscrire auprès du site et être authentifié.</p>
        <h3>Système de gestion des configurations</h3>
        <p>M2L gère à travers un logiciel de gestion des configurations l'ensemble du parc informatique incluant les postes fixes des ligues.</p>
        <h3>Intégration d'imprimantes</h3>
        <p>Lorsque les structures hébergées s'équipent d'imprimante réseau, la connexion en est réalisée par l'informaticien bénévole du CROSL, sans passer par un serveur d'impression. Le nom de l'imprimante est lui aussi codifié de la même façon que celui des postes (la lettre L vient remplacer la lettre P).</p>
        <h3>Formations</h3>
        <p>Le CROSL offre un catalogue diversifié de formations aux bénévoles des clubs affiliés aux ligues (législation, éthique, comptabilité associative, etc.). Les ligues organisent également des formations, en général plus techniques, sur l'usage de logiciels spécifiques de gestion des clubs ou des compétitions sportives.</p>
        <h3>Service d'établissement de bulletins de salaire</h3>
        <p>Le CRIB (Centre Régional d'Information des Bénévoles) est un label donné au CROSL qui, entre autres missions d'information (sur les textes réglementaires, la convention nationale du sport ...), propose un service d'établissement de bulletins de salaires aux ligues et à leurs clubs affiliés. Le CROSL est tiers de confiance pour l'URSSAF et, à ce titre, établit des bulletins de salaires réglementaires et tous les documents annexes. La prestation est facturée au forfait (60 € par an) et au bulletin édité (5 €). Un employé du CROSL est affecté à cette mission à raison de 0,8 ETP</p>

        <h2>Des financements</h2>
        <h3>Statut juridique et financement M2L</h3>
        <p>La Maison des Ligues est un établissement du Conseil Régional. Ce n’est pas une entité juridique en propre. Elle est financée à 100 % (pour son fonctionnement et pour la construction récente de l’extension des bâtiments C et D) par le Conseil Régional et sans aucune participation du Conseil Général de Meurthe et Moselle, bien qu’elle abrite un certain nombre de comités départementaux. Une convention de cogestion a été passée entre le Conseil Régional et le Comité Régional Olympique et Sportif de Lorraine pour la gestion de l’outil « Maison des Ligues ». Le CROSL est une association financée par le ministère via le CNDS (Centre National de Développement du Sport).l</p>
        <h3>Loyer demander au "locatairs"</h3>
        <p>La M2L est davantage sur le principe d’une participation aux charges de gestion courante, plutôt que sur celui d’un loyer à proprement parler. On est sur la base de 5€ par m2 et par mois, avec une augmentation prévue d’un euro dans les mois à venir. Un appel est effectué sur cette base forfaitaire en début d’année et une régularisation pourrait être demandée en fin d’année en cas de dépassement de la consommation électrique, mais cela n’a jamais été fait jusqu’à maintenant.</p>
        <p>La réalité des charges à répartir est difficile à cerner. Si vous prenez par exemple le poste « nettoyage des locaux » : celui-ci fait l’objet d’un appel d’offres du Conseil Régional pour l’ensemble de ses implantations. Il est difficile de savoir quelle est la part affectée à la Maison et encore moins la part qui incomberait aux structures hébergées. Il n’y a pas eu jusqu’à maintenant de comptabilité analytique dans ce domaine</p>
        <p>Les charges comprennent le chauffage, l’électricité, le nettoyage, l’accès Internet ; le téléphone n’en fait pas partie. Les consommations sont refacturées chaque trimestre aux structures hébergées. Depuis la mise en place de la téléphonie IP fin 2010, on peut connaître les consommations poste par poste et même structure par structure. Les appareils téléphoniques font partie de la prestation d’hébergement, comme la fourniture de trois badges pour l’entrée et de deux clés par bureau.</p>
        <h3>Facturation des prestations d'hebergement et annexes aux structures</h3>
        <p>Nous sommes dans le cadre d’une convention de cogestion entre le CROSL et la Région Lorraine, mais il n’est pas question que le CROSL facture l’utilisation de ressources appartenant à la Région, comme les locaux ou le téléphone par exemple. Par contre, le CROSL facture des prestations annexes réalisées sur des équipements lui appartenant, comme l’affranchissement et les tirages sur les photocopieurs numériques.</p>
        <h3>Niveaux de tarification</h3>
        <p>Les ligues peuvent réserver sans payer jusqu’à concurrence de six réservations par an, hors amphi. Les clubs sportifs et les comités départementaux ont un premier niveau de tarification. Les associations, les lycées ou encore les collèges sont sur un second niveau de tarification. Enfin, tous les autres organismes, y compris des sociétés privées, sont sur le niveau de tarification le plus haut. Pour toutes les structures qui ne sont pas hébergées, les locaux étant publics, il faut faire signer entre les parties une « convention d’occupation temporaire ». La réservation se fait dans l’Intranet directement pour les structures hébergées ou par le secrétariat pour une demande extérieure. Les informations sont ensuite transmises au Conseil Régional qui émet un « titre de paiement », c’est-à-dire une facture d’occupation de locaux. Ces titres sont envoyés directement par le Conseil Régional aux utilisateurs.</p>


